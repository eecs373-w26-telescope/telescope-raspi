constexpr uint16_t SYNC_WORD = 0xABCD;
constexpr uint8_t SYNC_HI = 0xAB;
constexpr uint8_t SYNC_LO = 0xCD;
constexpr uint8_t MAX_PAYLOAD_SIZE = 128;

// All packets: Nucleo -> Raspi
// Bidirectional: 0xFF
enum PacketId : uint8_t {
	PACKET_GPS          = 0x01,
	PACKET_ENCODER      = 0x02,
	PACKET_IMU          = 0x03,
	PACKET_STATE_SYNC   = 0x04,
	PACKET_DSO_TARGET   = 0x05,
	PACKET_DEBUG        = 0xFF,
};

enum DsoObjectType : uint8_t {
	DSO_GALAXY           = 0,
	DSO_NEBULA           = 1,
	DSO_OPEN_CLUSTER     = 2,
	DSO_GLOBULAR_CLUSTER = 3,
	DSO_PLANETARY_NEBULA = 4,
};

enum DsoStatus : uint8_t {
	DSO_OK        = 0,
	DSO_NOT_FOUND = 1,
	DSO_SD_ERROR  = 2,
};

#pragma pack(push, 1)

struct PacketHeader {
	uint16_t sync;
	uint8_t  packet_id;
	uint8_t  length;
};

struct GpsPayload {
	int32_t  latitude_e7;
	int32_t  longitude_e7;
	uint8_t  num_satellites;
	uint8_t  utc_hour;
	uint8_t  utc_minute;
	uint8_t  utc_second;
	uint8_t  utc_day;
	uint8_t  utc_month;
	uint16_t utc_year;
};

struct EncoderPayload {
	uint16_t yaw_raw;
	uint16_t pitch_raw;
};

struct ImuPayload {
	int16_t  heading;
	uint8_t  calibration;
};

struct StateSyncPayload {
	uint8_t  state;
	uint8_t  flags;
	uint16_t sequence;
};

struct DsoTargetPayload {
	uint8_t  status;
	uint16_t catalog_number;
	uint8_t  object_type;
	int32_t  ra_mas;
	int32_t  dec_mas;
	int16_t  magnitude_e2;
	uint8_t  constellation;
	char     name[16];
};

struct DebugPayload {
	uint8_t data[16];
};

#pragma pack(pop)

